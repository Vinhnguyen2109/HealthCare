package com.example.healthcare.model;

public class TrieuChung {
    private int ngay;
    private String mota;
    private int taikham;

    public int getNgay() {
        return ngay;
    }

    public void setNgay(int ngay) {
        this.ngay = ngay;
    }

    public String getMota() {
        return mota;
    }

    public void setMota(String mota) {
        this.mota = mota;
    }

    public int getTaikham() {
        return taikham;
    }

    public void setTaikham(int taikham) {
        this.taikham = taikham;
    }
}
